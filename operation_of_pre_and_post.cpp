#include<iostream>
using namespace std;
int main(){
    int a;
    cout <<"Enter the Number Which you want to perform the operations :"<<endl;
    cin>>a;
    a++; 
    cout<<"this will increment the number :"<<a<<endl;
    ++a;
    cout<<"This will increment the number :"<<a<<endl;
    a=a+1;
    cout<<"This will increment the number :"<<a<<endl;
    a--;
    cout<<"This will decrease the number :"<<a<<endl;
    --a;
    cout<<"This will decrease the number :"<<a<<endl;
    a=a-1;
    cout<<"This will decrease the number :"<<a<<endl;
    return 0;
}